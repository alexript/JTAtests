    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if (isBusy()) {
            return;
        }
        if (client != null) {
            try {
                client.close();
            } catch (Exception ex) {
                Logger.getLogger(ApplicationFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        beforeExit();
        props.writeProps();
        System.exit(0);
    }//GEN-LAST:event_formWindowClosing

    private void mainloopToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mainloopToggleButtonActionPerformed
        if (isBusy()) {
            return;
        }
        flagExecution(true);
        mainloopToggleButton.setEnabled(false);
        new Thread(() -> {
            client.mainLoop();
            flagExecution(false);
            java.awt.EventQueue.invokeLater(() -> {
                mainloopToggleButton.setSelected(false);
                mainloopToggleButton.setEnabled(true);
            });
        }).start();

    }//GEN-LAST:event_mainloopToggleButtonActionPerformed

    void beforeExit() {

        props.setDividerPosition(jSplitPane1.getDividerLocation());
        int state = this.getExtendedState();
        boolean isMax = (state & JFrame.MAXIMIZED_BOTH) != 0;
        props.setMaximized(isMax);
        if (!isMax) {
            Dimension size = this.getSize();
            int x = this.getX();
            int y = this.getY();
            props.setX(x);
            props.setY(y);
            props.setSize(size);
        }
    }

